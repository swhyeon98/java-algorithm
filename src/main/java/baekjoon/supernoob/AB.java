package baekjoon.supernoob;

import java.util.Scanner;
public class AB {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
//        int a = sc.nextInt();
//        int b = sc.nextInt();
//        System.out.print(a+b);
        System.out.println(sc.nextInt()+sc.nextInt());  // A+B
        System.out.println(sc.nextInt()-sc.nextInt());  // A-B
        System.out.println(sc.nextInt()*sc.nextInt());  // A*B
        System.out.println(sc.nextDouble()/sc.nextDouble());  // A/B
    }
}
